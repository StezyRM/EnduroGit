// Сортировка
function sortMotorcycles(list, sortBy) {
    switch (sortBy) {
        case 'price-asc':
            return list.sort((a, b) => a.price - b.price);
        case 'price-desc':
            return list.sort((a, b) => b.price - a.price);
        case 'engine-asc':
            return list.sort((a, b) => a.engine - b.engine);
        case 'engine-desc':
            return list.sort((a, b) => b.engine - a.engine);
        default:
            return list;
    }
}

// Фильтрация
function filterMotorcycles() {
    const priceFrom = parseInt(document.getElementById('priceFrom').value) || 0;
    const priceTo = parseInt(document.getElementById('priceTo').value) || Infinity;
    const engineValue = document.getElementById('engineSelect').value;
    const engineType = document.getElementById('engineType').value;

    return motorcycles.filter(moto => {
        const matchesPrice = moto.price >= priceFrom && moto.price <= priceTo;
        const matchesEngine = !engineValue || moto.engine == parseInt(engineValue);
        const matchesEngineType = !engineType || moto.engineType === engineType;

        return matchesPrice && matchesEngine && matchesEngineType;
    });
}

// Рендер карточек
function renderMotorcycles(list) {
    const container = document.getElementById('results');
    container.innerHTML = '';

    list.forEach(moto => {
        const card = document.createElement('div');
        card.className = 'moto-card';
        const formattedPrice = moto.price.toLocaleString('ru-RU');
        const engineTypeLabel = moto.engineType === "2t" ? "2-тактный" : "4-тактный";

        card.innerHTML = `
            <img src="${moto.images[0]}" alt="${moto.name}" loading="lazy">
            <div class="content">
                <h3>${moto.name}</h3>
                <p><strong>Мощность:</strong> ${moto.power} л.с.</p>
                <p><strong>Объём:</strong> ${moto.engine} см³</p>
                <p><strong>Двигатель:</strong> ${engineTypeLabel}</p>
                <div class="price">${formattedPrice} ₽</div>
                <p class="short-desc">${moto.shortDescription || moto.description.substring(0, 80)}...</p>
            </div>
        `;

        card.addEventListener('click', () => {
            openMotoModal(moto);
        });

        container.appendChild(card);
    });
}


// Инициализация
document.addEventListener('DOMContentLoaded', () => {
    const applyBtn = document.getElementById('applyFilters');
    if (applyBtn) {
        applyBtn.addEventListener('click', () => {
            let filtered = filterMotorcycles();
            const sortBy = document.getElementById('sortSelect').value;
            filtered = sortMotorcycles(filtered, sortBy);

            const container = document.getElementById('results');
            container.classList.add('empty');
            setTimeout(() => {
                renderMotorcycles(filtered);
            }, 300);
        });
    }

    // Сброс фильтров
    const resetBtn = document.getElementById('resetFilters');
    if (resetBtn) {
        resetBtn.addEventListener('click', () => {
            document.getElementById('priceFrom').value = '';
            document.getElementById('priceTo').value = '';
            document.getElementById('engineSelect').value = '';
            document.getElementById('engineType').value = '';
            document.getElementById('sortSelect').value = 'default';

            // Сбрасываем отображаемые метки
            document.getElementById('engineLabel').textContent = 'Любой';
            document.getElementById('engineTypeLabel').textContent = 'Любой';
            document.getElementById('sortLabel').textContent = 'По умолчанию';

            // Убираем выделение
            document.querySelectorAll('.dropdown-item').forEach(el => {
                el.classList.remove('selected');
            });

            renderMotorcycles(motorcycles);
        });
    }

    // Показать все при загрузке
    if (document.getElementById('results')) {
        renderMotorcycles(motorcycles);
    }
});

// Функция для страницы деталей — БЕЗ ДЕРГАНИЯ
function loadMotoDetail() {
    const urlParams = new URLSearchParams(window.location.search);
    const motoId = parseInt(urlParams.get('id'));

    const moto = motorcycles.find(m => m.id === motoId);
    if (!moto) {
        document.getElementById('motoDetail').innerHTML = '<h2>Мотоцикл не найден</h2>';
        return;
    }

    const engineTypeLabel = moto.engineType === "2t" ? "2-тактный" : "4-тактный";
    const formattedPrice = moto.price.toLocaleString('ru-RU');

    // Создаём HTML без анимаций и с фиксированными размерами
    let galleryHtml = '';
    moto.images.forEach((img, index) => {
        galleryHtml += `<img src="${img}" data-index="${index}" class="${index === 0 ? 'active' : ''}" loading="lazy">`;
    });

    let specsHtml = '';
    for (const [key, value] of Object.entries(moto.specs)) {
        specsHtml += `<tr><td>${key}</td><td>${value}</td></tr>`;
    }

    document.getElementById('motoDetail').innerHTML = `
        <div class="moto-detail-header">
            <h1>${moto.name}</h1>
            <div class="price">${formattedPrice} ₽</div>
        </div>

        <img src="${moto.images[0]}" class="main-photo" id="mainPhoto" loading="lazy">

        <div class="moto-gallery" id="gallery">
            ${galleryHtml}
        </div>

        <div class="moto-description">
            ${moto.description}
        </div>

        <h3>Технические характеристики</h3>
        <table class="specs-table">
            <tr><td>Тип двигателя</td><td>${engineTypeLabel}</td></tr>
            <tr><td>Объём двигателя</td><td>${moto.engine} см³</td></tr>
            <tr><td>Мощность</td><td>${moto.power} л.с.</td></tr>
            ${specsHtml}
        </table>
    `;

    // Галерея — без анимаций, мгновенная смена фото
    document.querySelectorAll('#gallery img').forEach(img => {
        img.addEventListener('click', function() {
            const index = this.getAttribute('data-index');
            document.getElementById('mainPhoto').src = moto.images[index];
            
            // Убираем active у всех
            document.querySelectorAll('#gallery img').forEach(el => el.classList.remove('active'));
            // Добавляем active текущему
            this.classList.add('active');
        });
    });
}

// Автозагрузка деталей при открытии страницы
if (window.location.pathname.includes('moto-detail.html')) {
    document.addEventListener('DOMContentLoaded', loadMotoDetail);
}


function openMotoModal(moto) {
    const modal = document.getElementById('modal');
    const modalBody = document.getElementById('modalBody');
    const engineTypeLabel = moto.engineType === "2t" ? "2-тактный" : "4-тактный";
    const formattedPrice = moto.price.toLocaleString('ru-RU');

    // Генерация галереи
    let galleryHtml = '';
    moto.images.forEach((img, index) => {
        galleryHtml += `<img src="${img}" data-index="${index}" class="${index === 0 ? 'active' : ''}" loading="lazy">`;
    });

    // Генерация характеристик
    let specsHtml = '';
    for (const [key, value] of Object.entries(moto.specs)) {
        specsHtml += `<tr><td>${key}</td><td>${value}</td></tr>`;
    }

    // Текущий индекс фото
    let currentIndex = 0;

    // HTML модального окна
    modalBody.innerHTML = `
        <h2>${moto.name}</h2>
        <div class="price" style="font-size: 1.5rem; color: #ff6b6b; margin: 10px 0;">${formattedPrice} ₽</div>

        <div class="modal-photo-container">
            <img src="${moto.images[0]}" class="modal-main-photo" id="modalMainPhoto">
            <button class="modal-arrow prev" id="modalPrev"><</button>
            <button class="modal-arrow next" id="modalNext">></button>
        </div>

        <div class="modal-gallery" id="modalGallery">
            ${galleryHtml}
        </div>

        <div class="modal-description">
            ${moto.description}
        </div>

        <div class="modal-specs">
            <h3>Технические характеристики</h3>
            <table>
                <tr><td>Тип двигателя</td><td>${engineTypeLabel}</td></tr>
                <tr><td>Объём двигателя</td><td>${moto.engine} см³</td></tr>
                <tr><td>Мощность</td><td>${moto.power} л.с.</td></tr>
                ${specsHtml}
            </table>
        </div>

        <a href="${moto.dealerLink}" target="_blank" class="btn-dealer">
            Подробнее у дилера →
        </a>
    `;

    // Элементы
    const mainPhoto = document.getElementById('modalMainPhoto');
    const prevBtn = document.getElementById('modalPrev');
    const nextBtn = document.getElementById('modalNext');
    const galleryImgs = document.querySelectorAll('#modalGallery img');

    // Обновление фото
    function updateMainPhoto(index) {
        currentIndex = index;
        mainPhoto.src = moto.images[index];
        galleryImgs.forEach((img, i) => {
            img.classList.toggle('active', i === index);
        });
    }

    // Стрелки
    prevBtn.onclick = () => {
        const newIndex = currentIndex === 0 ? moto.images.length - 1 : currentIndex - 1;
        updateMainPhoto(newIndex);
    };

    nextBtn.onclick = () => {
        const newIndex = currentIndex === moto.images.length - 1 ? 0 : currentIndex + 1;
        updateMainPhoto(newIndex);
    };

    // Миниатюры
    galleryImgs.forEach(img => {
        img.onclick = function() {
            const index = parseInt(this.getAttribute('data-index'));
            updateMainPhoto(index);
        };
    });

    // Показ модалки
    modal.style.display = 'block';

    // Закрытие
    document.querySelector('.close').onclick = () => {
        modal.style.display = 'none';
    };
    window.onclick = (e) => {
        if (e.target === modal) modal.style.display = 'none';
    };
}