// Функции для кастомных дропдаунов
function toggleDropdown(id) {
    const menu = document.getElementById(id);
    menu.classList.toggle('show');

    // Закрываем другие меню
    document.querySelectorAll('.dropdown-menu').forEach(m => {
        if (m.id !== id) m.classList.remove('show');
    });
}

function selectOption(element, labelId, inputId) {
    const label = document.getElementById(labelId);
    const input = document.getElementById(inputId);
    const value = element.getAttribute('data-value');

    label.textContent = element.textContent;
    input.value = value;

    // Убираем активность у других элементов
    const parentMenu = element.closest('.dropdown-menu');
    parentMenu.querySelectorAll('.dropdown-item').forEach(el => {
        el.classList.remove('selected');
    });
    element.classList.add('selected');

    // Закрываем меню
    parentMenu.classList.remove('show');
}

